import { random } from 'lodash';


const generateRandomDate = (postId) => {
  const localStorageKey = `randomDates_${postId}`;
  const storedDates = localStorage.getItem(localStorageKey);
  
  if (storedDates) {
    return JSON.parse(storedDates);
  } else {
    const startDate = new Date(2022, 0, 1).getTime(); // Start date in milliseconds (e.g., January 1, 2022)
    const endDate = new Date().getTime(); // Current date in milliseconds

    const createdDate = new Date(random(startDate, endDate));
    const updatedDate = new Date(random(createdDate.getTime(), endDate));
    
    const randomDates = {
      created_at: createdDate.toISOString(),
      updated_at: updatedDate.toISOString(),
    };

    localStorage.setItem(localStorageKey, JSON.stringify(randomDates));

    return randomDates;
  }
};

